/**
 * システムプロンプトに機能別ルールを追加
 */
export function buildFeaturePrompt(featureKey: string): string {
  const featureRules = AI_VP_FEATURE_RULES[featureKey];
  if (!featureRules) {
    return AI_VP_SYSTEM_PROMPT;
  }

  const rulesText = featureRules.map((rule, idx) => `${idx + 1}. ${rule}`).join('\n');

  return `${AI_VP_SYSTEM_PROMPT}

【この機能の追加ルール】
${rulesText}`;
}
